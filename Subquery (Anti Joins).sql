-- Anti Joins (Subquery)
SELECT
	country,
    continent
FROM states
WHERE continent NOT IN (SELECT
							continent
						FROM states
						WHERE indep_year < 1800) AND continent LIKE '%America';

                    
                      


    








    
































    


















    
    
    
    
    
    
    


































    

    

    











    

 








    















