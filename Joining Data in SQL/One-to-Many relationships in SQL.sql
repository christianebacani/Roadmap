USE practicedb;
-- Creating a One-to-Many Relationships in MySQL
CREATE TABLE esports_matches(match_id INT PRIMARY KEY, match_name VARCHAR(255));

CREATE TABLE esports_players(player_id INT PRIMARY KEY, player_name VARCHAR(255), match_id INT, FOREIGN KEY(match_id) REFERENCES esports_matches(match_id));

