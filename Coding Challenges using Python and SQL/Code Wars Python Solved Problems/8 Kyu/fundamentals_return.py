# Question: Fundamentals: Return
# Categories: 8 Kyu

def add(a: int, b: int) -> int:
    return a + b

def multiply(a: int, b: int) -> int:
    return a * b

def divide(a: int, b: int) -> int:
    return a / b

def mod(a: int, b: int) -> int:
    return a % b

def exponent(a: int, b: int) -> int:
    return a ** b

def subt(a: int, b: int) -> int:
    return a - b