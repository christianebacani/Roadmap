-- Querying the relationships of the tables
SELECT
	*
FROM departments AS d
INNER JOIN teachers AS t
	USING(department_id)
INNER JOIN students AS s
	ON s.teacher_id = t.teacher_id
    AND d.school_name = s.school_name;







    


    


    
    





    










    





    




















































































    













                                        
                                        






























































































    

























    
    
    
    
    












    















    









































    


    


    

















    

























    



    
    
    









































    













                                        
                                        






























































































    

























    
    
    
    
    












    















    
























    


    


    
    





    










    





    




















































































    













                                        
                                        






























































































    

























    
    
    
    
    












    















    









































    


    


    

















    

























    



    
    
    









































    













                                        
                                        






























































































    

























    
    
    
    
    












    















    


















