USE practicedb;
-- Multiple Joins in MySQL

-- Creating the table (One-to-Many Relationships)
CREATE TABLE departments(department_id INT PRIMARY KEY, department_name VARCHAR(255), school_name VARCHAR(255));

CREATE TABLE teachers(teacher_id INT PRIMARY KEY, department_id INT, teacher_name VARCHAR(255), school_name VARCHAR(255), FOREIGN KEY (department_id) REFERENCES departments(department_id));

CREATE TABLE students(student_id INT PRIMARY KEY, teacher_id INT, student_name VARCHAR(255), school_name VARCHAR(255), FOREIGN KEY (teacher_id) REFERENCES teachers(teacher_id));

-- Inserting the records in our tables
