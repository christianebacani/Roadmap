-- Querying the tables
SELECT * FROM malls
INNER JOIN malls_products
	USING(mall_id);
    
SELECT * FROM products
INNER JOIN malls_products
	USING(product_id);
    



 










































    













                                        
                                        






























































































    

























    
    
    
    
    












    















    









































    


    


    

















    

























    



    
    
    









































    













                                        
                                        






























































































    

























    
    
    
    
    












    















    


















