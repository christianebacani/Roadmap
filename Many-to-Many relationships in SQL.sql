-- Use any of your database
USE countries;


-- Creating  Many-to-Many Relationships in MySQL
CREATE TABLE malls(mall_id INT, mall_name VARCHAR(255));

CREATE TABLE products(product_id INT, product_name VARCHAR(255));


-- Creating indexes for query performance and for foreign key constraints
CREATE INDEX INDEX_MALL_ID ON malls(mall_id);

CREATE INDEX INDEX_PRODUCT_ID ON products(product_id);

CREATE TABLE malls_products(mall_product_id INT PRIMARY KEY, mall_id INT, product_id INT, FOREIGN KEY (mall_id) REFERENCES malls(mall_id), FOREIGN KEY (product_id) REFERENCES products(product_id));

-- Inserting the records insidea Many-to-Many Relationship table
