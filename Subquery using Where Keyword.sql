USE countries;

-- Subquery using Where Clause
SELECT
	country_name
FROM countries
WHERE code IN (SELECT
					country_code
			   FROM populations
               WHERE life_expectancy > (SELECT
											AVG(life_expectancy)
										FROM populations))
ORDER BY country_name;
-- Query all the country that the life expectancy is greater than the global average of life expectancy




    




    


















































                        



                    
                      


    








    
































    


















    
    
    
    
    
    
    


































    

    

    











    

 








    















