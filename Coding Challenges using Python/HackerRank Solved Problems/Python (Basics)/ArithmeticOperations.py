# Sum Function
def sum(x, y):
    return x + y

# Subtract function
def subtract(x, y):
    return x - y

# Multiple Function
def multiply(x, y):
    return x * y

# Main function
if __name__ == '__main__':
    a = int(input())
    b = int(input())

    # Execute the functions
    print(sum(a, b))
    print(subtract(a, b))
    print(multiply(a, b))



